int ft_is_prime(int nb){
	if(nb < 2)
		return 0;

	if(nb == 2)
		return 1;

	if(nb > 2)
	{
		int i = nb;
		while( i >= 2){
			if( nb%i == 0){
				return 0;
				i--;
			}
		}
		return 1;
	}
}



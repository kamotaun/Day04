int ft_sqrt(int nb){
	if(nb < 0)
		; 
	if (nb == 0 || nb == 1)
		return nb;
	if(nb > 1)
	{
		int i = 2;
		while( i <= nb )
		{
			if( nb = i*i)
				return i;
			i--;
		}
	}
}

